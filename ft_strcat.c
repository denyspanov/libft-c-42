#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t n;
	size_t i;

	i = 0;
	n = ft_strlen(s1);
	while (s2[i])
	{
		s1[i + n] = s2[i];
		i++;
	}
	s1[i + n] = '\0';
	return (s1);
}
