#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int res[3];

	res[0] = 0;
	res[1] = 1;
	res[2] = 1;
	i = -1;
	while ((str[++i] <= '9' && str[i] >= '0')
		|| (str[i] == ' ' && res[1]) ||
		(str[i] >= 9 && str[i] <= 13 && res[1]) || (str[i] == '-' && res[1])
		|| (str[i] == '+' && res[1]))
	{
		if ((str[i] <= ' ') || (str[i] >= 9 && str[i] <= 13 && res[1]))
			continue ;
		if (str[i] == '-')
			res[2] = -1;
		if ((str[i] <= '9' && str[i] > '0') || (str[i] == '0' && !res[1]))
			res[0] = res[0] * 10 + str[i] - 48;
		res[1] = 0;
	}
	return (res[0] * res[2]);
}
